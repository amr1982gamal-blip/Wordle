    /**
     * Unifies and normalizes Arabic characters for friendly comparison
     */
    fun normalizeChar(c: Char): Char {
        return when (c) {
            'أ', 'إ', 'آ', 'ٱ', 'ء' -> 'ا'
            'ة' -> 'ه'
